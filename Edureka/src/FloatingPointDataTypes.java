
public class FloatingPointDataTypes {

	public static void main(String[] args) {
		//float sal = 2500.5f;
		//float sal = 2500.5F;
		float sal = (float)2500.5;
		System.out.println(sal);
		
		double d = 2500.5;
		System.out.println("My Salary = "+ d);
		
		int n = (int)2500.5;
		System.out.println(n);
	}

}
